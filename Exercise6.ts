// Write a program that calculates the result of a mathematical expression.	((10 + 5) * 3 - 2) / (4 % 3)  - 7)

let n1 = 10;
let n2 = 5;
let n3 = 3;
let n4 = 2;
let n5 = 4;
let n6 = 3;
let n7 = n5 % n6;
let n8 = 7;

let solution = ((n1 + n2) * n3 - n4) / n7 - n8;
